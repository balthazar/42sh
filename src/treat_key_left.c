#include "42sh.h"

int					treat_key_left(void)
{
	if (CTX->i > 0)
	{
		tputs(tgetstr("le", NULL), 1, ft_putput);
		--CTX->i;
		--CTX->pos.x;
	}
	return (OK);
}
